package SimpliLearn;

public class BitwiseOperation {
	public static void main(String args[])
	{
		int a=5;
		int b=7;
		
		//bitwise AND 
		//0101 & 0111 = 0101
		System.out.println("a&b :" + (a & b));
		
		//bitwise OR 
				//0101 | 0111 = 0111
				System.out.println("a|b :" + (a | b));
	}
}
